package gr.seo.youthapp.model.enums;

/**
 * Enum representing the types of pins in the system. Each pin type has a distinct purpose:
 * <un>
 * <li>PIN: Represents a regular pin (PTYXIO).</li>
 * <li>GUIDING_PIN: Represents a guiding pin (ODIGISMOU), A, B or C, which are distinct for the
 * related badge to be completed</li>
 * </un>
 */
public enum PinType {
  PIN, GUIDING_PIN
}
