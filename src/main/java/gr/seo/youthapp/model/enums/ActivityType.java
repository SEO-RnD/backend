package gr.seo.youthapp.model.enums;

/**
 * Enum representing the types of activities available in the system. Each activity type serves a
 * distinct purpose and is categorized as:
 * <ul>
 *   <li>PATH: Represents the path activities, which could involve progressive tasks or sequences.</li>
 *   <li>PIN_ACTIVITY: Represents an activity that is required for completing a pin.</li>
 * </ul>
 */
public enum ActivityType {
  PATH, PIN_ACTIVITY
}
